# ----------------------------------------
st.title("ðŸ“… Genetic Algorithm Scheduler")
st.markdown("""
This app allows users to experiment with **Crossover Rate (CO_R)** and **Mutation Rate (MUT_R)** 
to see how they affect the final schedule generated by a Genetic Algorithm.
""")

# Sidebar inputs for parameters (Part 3)
st.sidebar.header("Set Genetic Algorithm Parameters")

CO_R = st.sidebar.slider("Crossover Rate (CO_R)", 0.0, 0.95, 0.8, 0.01)
MUT_R = st.sidebar.slider("Mutation Rate (MUT_R)", 0.01, 0.05, 0.02, 0.01)

st.sidebar.write("Crossover Rate:", CO_R)
st.sidebar.write("Mutation Rate:", MUT_R)

# Load data
file_path = "program_ratings_modified.csv"
ratings = read_csv_to_dict(file_path)
all_programs = list(ratings.keys())
all_time_slots = list(range(6, 6 + len(all_programs)))  # time slots: 6 AM onwards

# Run algorithm when user clicks button
if st.button("Run Genetic Algorithm"):
    initial_schedule = all_programs.copy()
    random.shuffle(initial_schedule)

    best_schedule = genetic_algorithm(
        initial_schedule,
        ratings,
        generations=100,
        population_size=50,
        crossover_rate=CO_R,
        mutation_rate=MUT_R,
        elitism_size=2,
        all_programs=all_programs
    )

    total_fitness = fitness_function(best_schedule, ratings)

    # Display schedule in table (Part 4)
    st.subheader("âœ… Generated Schedule")
    df = pd.DataFrame({
        "Time Slot": [f"{t}:00" for t in all_time_slots],
        "Program": best_schedule
    })
    st.table(df)

    st.success(f"Total Ratings (Fitness Score): {total_fitness:.2f}")

